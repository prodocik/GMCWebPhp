            <ul id="generator" class="cd-faq-group">
                <li class="cd-faq-title">
                    <h2>Generator</h2></li>
                <li>
                    <a class="cd-faq-trigger" href="#0">What    type    of  fuel    does    my  generator   use?</a>
                    <div class="cd-faq-content">
                        <p>Cambria, BT, Nexus, Vegas: All gas chassis RV's have a gasoline generator that pulls gas from the vehicle's main fuel tank. When you refill the vehicle's gas tank, you are also refilling the generator's tank because they are the same tank. Keep in mind that generator usage will affect any MPG calculations that you do.
                            <br>View only: The Winnebago View has a diesel generator that pulls diesel from the vehicle's main fuel tank. When you refill the vehicle's diesel tank, you are also refilling the generator's tank because they are the same tank. Keep in mind that generator usage will affect any MPG calculations that you do.
                            <br>Solera and Gemini: The Forest River Solera and Thor Gemini are equipped with a propane generator. This generator pulls propane from the propane tank and does not affect vehicle MPG calculations. </p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">What    happens if  I   run the generator   too long    and use up  all my  fuel?</a>
                    <div class="cd-faq-content">
                        <p>View and Gas RV's: the generator will run out of fuel when the vehicle's fuel tank reaches around Â¼ tank. This is by design to leave you with enough fuel to reach a service station.
                            <br>Solera and Gemini: the generator in this vehicle is propane. You will not be stranded if you use up all the propane, but your propane appliances will not work. Most notably, your refrigerator will not work when 120VAC power is not available. </p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">Will    generator   exhaust make    my  campsite    stink (for  campsites   that    do  not have    power)?</a>
                    <div class="cd-faq-content">
                        <p>View and Gas RV's: Possibly. These RV's have diesel and gasoline generators respectively and diesel/gas fumes have a noticeable odor. We recommend shutting all doors and windows when using the generator in these vehicles.
                            <br>Solera and Gemini: No. The generator is a clean burning propane generator that emits only water vapor. </p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
            </ul>
            <!-- cd-faq-group -->
            <ul id="propane" class="cd-faq-group">
                <li class="cd-faq-title">
                    <h2>Propane</h2></li>
                <li>
                    <a class="cd-faq-trigger" href="#0">How much    propane will    I   use?</a>
                    <div class="cd-faq-content">
                        <p>It depends on what you do and which vehicle you are in.
                            <br>The Solera and Gemini are equipped with a propane generator. If you are camping without hookups in hot weather with heavy generator and AC use, you may need to refill after just 2 days. On the other hand, if you have hookups at night and just do some daily driving with the generator off, you can easily go a month without refilling. The largest consumers of propane are the generator (Solera only) and the heating system. Although the refrigerator consumes propane when electricity is not available, its propane consumption is surprising low - almost negligible. </p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">Where   can I   buy propane?</a>
                    <div class="cd-faq-content">
                        <p>There are many places to buy propane, including certain RV parks, gas stations, etc. The easiest way to find a location nearby that sells propane is to go to <a href="http:/www.uhaul.com">www.uhaul.com</a>. Most UHaul locations sell propane at reasonable prices and their website will not only help you find a nearby UHaul with propane, but it will even give you the current price. Most Tractor Supply Stores also sell propane and at lower prices than UHaul. When returning your RV, the Ace Hardware between our office and I-20 has propane.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">What can I do to conserve propane?</a>
                    <div class="cd-faq-content">
                        <p>First and foremost, turn off the generator as much as possible (Solera/Gemini only)! Even if your generator does not use propane, it is still a good idea to conserve fuel by turning it off when not needed. Leave the generator off while driving and use the dash AC and heat. In some RVs you can set the main HVAC to "fan only" to help circulate the air; the fan uses 12V power and will run without the generator. When plugged in, use electricity as much as possible for cooking, heating water, heating the RV, etc. Most water heater will heat with electric only when the heater is switched off. Try leaving the water heater off and let electricity heat the water when plugged in.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">Solera/Gemini Only: I am  almost out of propane, I don't have hookups, my batteries are low, and it's hot/cold outside. What can I do?</a>
                    <div class="cd-faq-content">
                        <p>Crank the engine and use the dash AC/heat. The engine will burn diesel instead of propane and will charge your batteries. Set the main HVAC to "fan only" to help circulate the air. You will not have 120V AC, but in this case, it is better to conserve your propane so your refrigerator operates. Burning diesel at idle will not cost any more than running the propane generator, but monitor your diesel consumption. Don't run out of fuel and leave yourself stranded.</p>
                    </div>
