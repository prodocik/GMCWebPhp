                <li class="cd-faq-title">
                    <h2>General</h2></li>
                <li>
                    <a class="cd-faq-trigger" href="#0">How can I find good campsites?</a>
                    <div class="cd-faq-content">
                        <p>Two good sites for locating campsites are <a href="http://www.woodalls.com">www.woodalls.com</a> and <a href="http://www.reserveamerica.com">www.reserveamerica.com</a>. Woodall's gives information about all campsites - public and private. Reserve America processes online booking for sites in their system, which is mostly government owned sites (federal, state, and some municipal) and some private parks.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">Do  I   need    to  book    a   campsite    in  advance?</a>
                    <div class="cd-faq-content">
                        <p>Booking in advance ensures that you get the best sites at the best prices. During the summer and on weekends and holidays, it is very important to book early. Campsites fill up well in advance during these times. Florida campgrounds fill up during the winter months, so advanced booking is highly recommended for winter camping in Florida.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">It  is  late    and I   don't   have    a   place   to  camp.   What    can I   do?</a>
                    <div class="cd-faq-content">
                        <p>GPS's can help you find a campground or a Walmart. Walmart is known to allow RVers to use their parking lots overnight. It is a good idea to check with the store manager first. Never stay more than 1 night when doing this, and arrive late and leave early. If you have any shopping needs this can be especially handy-reward the store with your business. Another option is rest stops and truck stops. When pulling into a rest stop for the night, pull into the "car" area rather than the "truck" area. It will be quieter and the truckers will appreciate you leaving the bigger spaces for them. If there is a RV area (very few rest areas have this) use it. Some states, such as Florida, only allow overnight stays at campgrounds. It is best to plan ahead and book campsites in advance.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">When    do  I   need    to  arrive  at  my  campsite?</a>
                    <div class="cd-faq-content">
                        <p>Be sure to make a note of campsite rules when booking. Many campsites only allow you to arrive during business hours. For some, you can arrive late but you may need to call during office hours to get a gate code. If you are going to arrive after 5, call them and make sure it is OK.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">Where   can I   dump    the waste   tanks?</a>
                    <div class="cd-faq-content">
                        <p>If you are at a campground use their facility. All RV campgrounds have dump stations. If you are on the road, many rest stops have them. Look for them on the ramps before re-entering the freeway. Sometimes travel centers (aka. truck stops) have them-many "Petro" and "TA" centers do. </p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
