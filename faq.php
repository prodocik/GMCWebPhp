            <ul id="electrical" class="cd-faq-group">
                <li class="cd-faq-title">
                    <h2>Electrical</h2></li>
                <li>
                    <a class="cd-faq-trigger" href="#0">How many    amps    is  the electrical  hookup?</a>
                    <div class="cd-faq-content">
                        <p>All Georgia Motorcoach RV's have 30 amp electrical Hookups. 50 amp electrical is typically only used for large RV's with multiple AC units.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">Can I   connect to  a   50  amp circuit?</a>
                    <div class="cd-faq-content">
                        <p>With the right adaptor, it is physically possible. 50 amps is more than the RV cables are designed to carry, and it is possible for the cables to be overloaded (eg, carrying 45 amps) without tripping the 50 amp breaker. Sites that have 50 amp circuits usually have 30 and 20 amp plugs. It is always safer to use the correct plug.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
                <li>
                    <a class="cd-faq-trigger" href="#0">Can I   plug    the RV  into    a   normal  household   (15 or  20  amp)    circuit?</a>
                    <div class="cd-faq-content">
                        <p>Yes, an adapter is provided to allow you to do this, but make sure that the main air conditioner and/or electric heat is switched off. The AC will overload the circuit and will probably trip the breaker after a few minutes. Gas heat is ok under this setup. This setup is good for running the refrigerator, lights, gas heat, and charging the batteries.</p>
                    </div>
                    <!-- cd-faq-content -->
                </li>
            </ul>
